export function getRandomMessages(count = 20): string[] {
  const n = Math.max(1, Math.min(count, COMFORT_MESSAGES.length));
  const shuffled = [...COMFORT_MESSAGES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}